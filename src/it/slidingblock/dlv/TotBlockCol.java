package it.slidingblock.dlv;
public class TotBlockCol
{
	int b, y;
	public int getB()
	{
		return b;
	}
	public void setB(int x)
	{
		this.b = x;
	}
	public int getY()
	{
		return y;
	}
	public void setY(int y)
	{
		this.y = y;
	}
	public TotBlockCol(int b, int y)
	{
		super();
		this.b = b;
		this.y = y;
	}
}
