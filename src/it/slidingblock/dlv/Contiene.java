package it.slidingblock.dlv;
public class Contiene
{
	int b;
	int x;
	int y;
	int t;
	public int getX()
	{
		return x;
	}
	public void setX(int x)
	{
		this.x = x;
	}
	public int getY()
	{
		return y;
	}
	public void setY(int y)
	{
		this.y = y;
	}
	public int getB()
	{
		return b;
	}
	public void setB(int b)
	{
		this.b = b;
	}
	public int getT()
	{
		return t;
	}
	public void setT(int t)
	{
		this.t = t;
	}
	public Contiene(int b, int x, int y, int t)
	{
		super();
		this.b = b;
		this.x = x;
		this.y = y;
		this.t = t;
	}
}